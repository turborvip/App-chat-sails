const io = require('socket.io');

module.exports = {
  someAction: function (req, res) {
    // Emit an event to all connected sockets
    io.sockets.emit('someEvent', { message: 'hello' });

    // Listen for an event from a particular socket
    req.socket.on('anotherEvent', (data) => {
      console.log(data);
    });
  }
};